/* 
Operadores de comparação

> Maior que
>= Maior ou igual a
< Menor que
<= Menor ou igual a
== Igual a (checa apenas o valor) = em desuso
=== Igual e do mesmo tipo (checa o valor e o tipo)
!= Diferente de (checa apenas o valor) = em desuso
!== Diferente, inclusive do tipo (checa o valor e o tipo)
*/

/* 
Operadores lógicos

&& - AND - "E" Todas as condições devem ser verdadeiras para que a condição final seja verdadeira.
|| - OR - "OU" Uma das condições deve ser verdadeira para que a condição final seja verdadeira.
! - NOT - "NÃO" Nega uma condição.

Valores que retornam false:
- false = único falso literal
- 0
- '' "" ``
- null / undefined
- NaN
*/

